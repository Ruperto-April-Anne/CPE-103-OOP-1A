#include<iostream> 
using namespace std;

int main ()
{ 
int i, j; 
cout<<"Enter two number: " ; 
cin >>i>>j; 
cout<<i<<" == "<<j<<" is "<<(i == j)<<'\n'; 
cout<<i<<" != "<<j<<" is "<<(i != j)<<'\n'; 
cout<<i<<" <= "<<j<<" is "<<(i <= j)<<'\n'; 
cout<<i<<" >= "<<j<<" is "<<(i >= j)<<'\n'; 
cout<<i<<" < "<<j<<" is "<<(i < j)<<'\n'; 
cout<<i<<" > "<<j<<" is "<<(i > j)<<'\n'; 
return 0; 
} 
